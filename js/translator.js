function gup(name) {
    name = name.replace(/[\[]/, "\\\[").replace(/[\]]/, "\\\]");
    var regexS = "[\\?&]" + name + "=([^&#]*)";
    var regex = new RegExp(regexS);
    var results = regex.exec(window.location.href);
    if (results == null)
        return "";
    return results[1];
}

var MODE = gup("mode");
var SIZE = MODE == "roguelike" ? 13 : Number.isInteger(parseInt(gup('size'))) ? parseInt(gup('size')) : 5;
var MAX_SLOT = MODE == "roguelike" ? 260 : SIZE <= 5 ? 25 : SIZE ** 2;

function translateToEN() {
    for (i = 1; i <= MAX_SLOT; i++) {
        var textElement = document.getElementById("slot" + i);
        Object.keys(bingoListJP).forEach(key => {
            if (bingoListJP[key]["Desc"] === textElement.textContent) {
                textElement.textContent = bingoListEN[key]["Desc"];
            }
        });
    }
    window.dispatchEvent(new Event("resize"));

    const params = new URLSearchParams(window.location.search);
    params.set('lang', 'en');
    history.replaceState('', '', '?' + params.toString());
};

